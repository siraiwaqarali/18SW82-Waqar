class BitwiseOperators{
public static void main(String[]args)
{
	int a=2;
	int b=3;
	System.out.println( "a&b= "+ (a&b) );
	System.out.println( "a|b= "+ (a|b) );
	System.out.println( "a^b= "+ (a^b) );
	System.out.println( "Left Shift= "+ (a<<1) );
	System.out.println( "Right Shift= "+ (b>>1) );
}
}